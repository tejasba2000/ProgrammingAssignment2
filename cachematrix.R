## These set of functions can cache inverse of a square matrix
## to avoid repeatition in calculation, thus saving time.

## This function returns a list containing function for
## getting and setting values of matrix and its inverse.

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) i <<- solve
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

## This function checks whether inverse of its argument matrix is cached or not.
## If inverse is cached, it returns cached value of inverse,
## otherwise it calculates inverse.

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}
